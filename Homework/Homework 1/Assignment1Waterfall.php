            <p>
                <b>Description:</b><br>
                The Waterfall Model was first Process Model to be introduced. 
                It is also referred to as a linear-sequential life cycle model. 
                It is very simple to understand and use. In a waterfall model, 
                each phase must be completed before the next phase can begin and there is no overlapping in the phases.<br>
                Waterfall model is the earliest SDLC approach that was used for software development.<br>
                The waterfall Model illustrates the software development process in a linear sequential flow; 
                hence it is also referred to as a linear-sequential life cycle model. 
                This means that any phase in the development process begins only if the previous phase is complete. 
                In waterfall model phases do not overlap. 
            </p>
            
            <p>
                <b>Advantages:</b><br>
                <ul>
                    <li>Simple and easy to understand and use</li>
                    <li>Easy to manage due to the rigidity of the model . each phase has specific deliverables and a review process.</li>
                    <li>Phases are processed and completed one at a time.</li>
                    <li>Works well for smaller projects where requirements are very well understood.</li>
                    <li>Clearly defined stages.</li>
                    <li>Well understood milestones.</li>
                    <li>Easy to arrange tasks.</li>
                    <li>Process and results are well documented.</li>
                </ul>
                <br>
            </p>
            
            <p>
                <b>Disadvantages:</b><br>
                <ul>
                    <li>No working software is produced until late during the life cycle.</li>
                    <li>High amounts of risk and uncertainty.</li>
                    <li>Not a good model for complex and object-oriented projects.</li>
                    <li>Poor model for long and ongoing projects.</li>
                    <li>Not suitable for the projects where requirements are at a moderate to high risk of changing. So risk and uncertainty is high with this process model.</li>
                    <li>It is difficult to measure progress within stages.</li>
                    <li>Cannot accommodate changing requirements.</li>
                    <li>Adjusting scope during the life cycle can end a project.</li>
                    <li>Integration is done as a "big-bang. at the very end, which doesn't allow identifying any technological or business bottleneck or challenges early.</li>
                </ul>
                <br>
            </p>
            
