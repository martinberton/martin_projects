            <p>
                <b>Description:</b><br>
                The spiral model combines the idea of iterative development with the systematic, controlled aspects of the waterfall model.<br>
                Spiral model is a combination of iterative development process model and sequential linear development model i.e. waterfall model with very high emphasis on risk analysis.<br>
                It allows for incremental releases of the product, or incremental refinement through each iteration around the spiral.
            </p>
            
            <p>
                <b>Advantages:</b><br>
                <ul>
                    <li>Changing requirements can be accommodated.</li>
                    <li>Allows for extensive use of prototypes</li>
                    <li>Requirements can be captured more accurately. </li>
                    <li>Users see the system early. </li>
                    <li>Development can be divided into smaller parts and more risky parts can be developed earlier which helps better risk management. </li>
                </ul>
                <br>
            </p>
            
            <p>
                <b>Disadvantages:</b><br>
                <ul>
                    <li>Management is more complex.</li>
                    <li>End of project may not be known early.</li>
                    <li>Not suitable for small or low risk projects and could be expensive for small projects.</li>
                    <li>Process is complex</li>
                    <li>Spiral may go indefinitely. </li>
                    <li>Large number of intermediate stages requires excessive documentation.</li>
                </ul>
                <br>
            </p>
            
            <p>
                The figure on the top right is a graphical representation of the various stages of a typical SDLC.
