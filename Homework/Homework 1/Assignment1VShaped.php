            <p>
                <b>Description:</b><br>
                The V - model is SDLC model where execution of processes happens in a sequential manner in V-shape. 
                It is also known as Verification and Validation model.
                <br>V - Model is an extension of the waterfall model and is based on association of a testing phase for each corresponding development stage. 
                This means that for every single phase in the development cycle there is a directly associated testing phase. 
                This is a highly disciplined model and next phase starts only after completion of the previous phase.
            </p>
            
            <p>
                <b>Advantages:</b><br>
                <ul>
                    <li>This is a highly disciplined model and Phases are completed one at a time.</li>
                    <li>Works well for smaller projects where requirements are very well understood.</li>
                    <li>Simple and easy to understand and use.</li>
                    <li>Easy to manage due to the rigidity of the model . each phase has specific deliverables and a review process.</li>
                </ul>
                <br>
            </p>
            
            <p>
                <b>Disadvantages:</b><br>
                <ul>
                    <li>High risk and uncertainty.</li>
                    <li>Not a good model for complex and object-oriented projects. </li>
                    <li>Poor model for long and ongoing projects.</li>
                    <li>Not suitable for the projects where requirements are at a moderate to high risk of changing.</li>
                    <li>Once an application is in the testing stage, it is difficult to go back and change a functionality </li>
                    <li>No working software is produced until late during the life cycle. </li>
                </ul>
                <br>
            </p>
            
